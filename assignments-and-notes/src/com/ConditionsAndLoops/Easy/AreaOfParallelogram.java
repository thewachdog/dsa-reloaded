package com.ConditionsAndLoops.Easy;

public class AreaOfParallelogram {
    public static void main(String[] args) {
        /*

        Area of parallelogram = b * h (same as rectangle but we need to calculate height)

         */
        int base = 9, height = 6, sideLength = 8;
        System.out.println(base * height);

        System.out.println("perimeter :" + 2 * (base + sideLength));

    }
}
